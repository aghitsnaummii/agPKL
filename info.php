<p>
 Hari ini, Jumat 29 februari 2024, ada keriuhan di SMP-SMK LUQMAN ALHAKIM KUDUS. Seluruh warga sekolah guru, murid, orangtua berpartisipasi dalam kegiatan Market Day tahun 2023. Kegiatan ini merupakan kali kedua yang diadakan oleh SMP-SMK ELHA setelah kegiatan Market Day pertama dilaksanakan pada tahun 2022 yang lalu.</p>
<p>
Peserta Market Day penjual kali ini adalah para santri dari kelas 9 sampai kelas 12. Sedangkan konsumen atau pembelinya adalah seluruh warga sekolah, termasuk orangtua/wali murid. Dari pagi hari, para santri dibantu oleh orangtua dan wali kelasnya, menyiapkan barang dagangannya di booth atau meja masing-masing yang telah ditentukan oleh panitia.</p>
<p>
Sehari-dua hari sebelumnya, Para santri bekerja membuat atau membeli barang-barang yang akan dijual pada kegiatan Market Day ini. Dalam hal ini terjadi kolaborasi dan kerja sama serta komunikasi antara para santri dengan orang tua mereka. Barang-barang yang dapat dijual dalam kegiatan Market Day ini adalah aneka jenis makanan, minuman, aksesories, dan ATK (Alat Tulis Kantor).</p>
<p>
Kegiatan Market Day ini di pusatkan di lapangan atau halaman SMP-SMK LUQMAN ALHAKIM KUDUS. Secara resmi kegiatan ini dibuka oleh Kepala Sekolah pada pukul 09.00 WIB. Dalam sambutannya, Kepala Sekolah menyampaikan bahwa kegiatan market day ini merupakan salah satu upaya untuk membangun dan menumbuhkan jiwa entrepreneurship atau kewirausahaan pada diri para santri sejak dini.Hal ini selaras dengan tema Market Day tahun ini yaitu "Membangun Jiwa Entrepreneurhsip Santri Sejak Dini".</p>
